package fr.be2.gsbapplication;

public class Synthese {
    String datefrais= null;
    String libelle = null;
    String montant = null;
    String datesaisie = null;
    String quantite = null;


    public String getDatefrais() {
        return datefrais;
    }
    public void setDatefrais(String datefrais) {
        this.datefrais = datefrais;
    }
    public String getLibelle() {
        return libelle;
    }
    public void setLibelle(String libelle) {
        this.libelle = libelle;
    }
    public String getMontant() {
        return montant;
    }
    public void setMontant(String montant) {
        this.montant = montant;
    }
    public String getDatesaisie() {
        return datesaisie;
    }
    public void setDatesaisie(String datesaisie) {
        this.datesaisie = datesaisie;
    }
    public String getQuantite() {
        return quantite;
    }
    public void setQuantite(String datefrais) {
        this.quantite = datefrais;
    }
}
